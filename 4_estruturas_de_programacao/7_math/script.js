let maior = Math.max(6,12,28,55,8); // Usando o método max do objeto Math para retornar o maior número do conjunto de números passado por parâmetro
console.log(maior); // Exibindo o resultado no console

let menor = Math.min(6,12,28,55,8); // Usando o método min do objeto Math para retornar o menor número do conjunto de números passado por parâmetro
console.log(menor);

let arredondar = Math.round(5.2934723); // Usando o método round do objeto Math para retornar o valor arredondado do número passado por parâmetro
console.log(arredondar);

let arredondarPraCima = Math.ceil(5.2934723); // Usando o método round do objeto Math para retornar o valor arredondado pra cima do número passado por parâmetro
console.log(arredondarPraCima);