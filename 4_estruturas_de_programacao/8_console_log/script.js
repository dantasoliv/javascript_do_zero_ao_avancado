let idade = 30; // Criando uma variável e atribuindo um valor para a mesma
let nome = "Daniel";

console.log(idade); // Exibindo o valor da váriável no console com a função console.log()
console.log(nome);

console.log(`Nome: ${nome}, Idade: ${idade}`); // Exibindo os valores das das variaveis dentro de uma string

console.log(`${typeof(idade)}, ${typeof(nome)}`); // Exibindo o tipo de dado da variável com a função console.log e a função typeof()